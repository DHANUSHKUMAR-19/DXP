// Cart helpers
window.VAUX.getCart = function() {
  try { return JSON.parse(sessionStorage.getItem('dxp_cart')) || []; } catch(e) { return []; }
};
window.VAUX.saveCart = function(cart) {
  sessionStorage.setItem('dxp_cart', JSON.stringify(cart));
};
window.VAUX.addToCart = function(productId, size, color, qty) {
  var cart = window.VAUX.getCart();
  var product = window.VAUX.PRODUCTS.find(function(p) { return p.id === productId; });
  if (!product) return;
  cart.push({ productId: productId, size: size, color: color, qty: qty || 1, price: product.price, name: product.name, brand: product.brand, image: product.image });
  window.VAUX.saveCart(cart);
  window.VAUX.updateCartBadge();
};
window.VAUX.updateCartBadge = function() {
  var badge = document.getElementById('cart-badge');
  if (!badge) return;
  var total = window.VAUX.getCart().reduce(function(s,i) { return s + i.qty; }, 0);
  badge.textContent = total;
  badge.style.display = total > 0 ? 'flex' : 'none';
};
window.VAUX.fmt = function(n) {
  return '₹' + Number(n).toLocaleString('en-IN');
};
window.VAUX.getProductById = function(id) {
  return window.VAUX.PRODUCTS.find(function(p) { return p.id === parseInt(id); });
};
